// Strategy: for each language block in the file, inject the missing keys
// right before the closing `},` of that block.

let out = src;

for (const [lang, newKeys] of Object.entries(patches)) {
  // Find the block: `  <lang>: {\n ... \n  },`
  // We insert new entries just before the closing `  },`
  const blockStartRe = new RegExp(`(  ${lang}: \\{[\\s\\S]*?)(\\n  \\},)`, 'm');
  const m = out.match(blockStartRe);
  if (!m) {
    console.error(`Could not find block for language: ${lang}`);
    continue;
  }

  // Build the new lines
  const lines = Object.entries(newKeys).map(([k, v]) => {
    // Escape single quotes in value (except already-escaped unicode)
    const escaped = v.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    return `    '${k}': '${escaped}',`;
  });

  const insertion = '\n' + lines.join('\n');
  out = out.replace(blockStartRe, `$1${insertion}$2`);
  console.log(`  ${lang}: +${Object.keys(newKeys).length} keys`);
}

